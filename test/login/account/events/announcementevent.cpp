#include <gtest/gtest.h>

#include "account/events/announcementevent.h"

using namespace openao::login::account::events;

TEST(AnnouncementEvent, Serialize) {

  // SETUP
  AnnouncementEvent event;
  event.expires_on_ts = 1893013624;
  event.message = "Welcome to the server!\nEnjoy your time :D";

  // EXECUTE
  BinaryBuffer result;
  result.write(event);

  // VALIDATE
  uint8_t expect[] = {0x78, 0x18, 0xd5, 0x70, 0x2a, 0x00, 0x00, 0x00, 0x57,
                      0x65, 0x6c, 0x63, 0x6f, 0x6d, 0x65, 0x20, 0x74, 0x6f,
                      0x20, 0x74, 0x68, 0x65, 0x20, 0x73, 0x65, 0x72, 0x76,
                      0x65, 0x72, 0x21, 0x0a, 0x45, 0x6e, 0x6a, 0x6f, 0x79,
                      0x20, 0x79, 0x6f, 0x75, 0x72, 0x20, 0x74, 0x69, 0x6d,
                      0x65, 0x20, 0x3a, 0x44, 0x00};

  ASSERT_EQ(0, std::memcmp(expect, result.data(), sizeof(expect)));
}